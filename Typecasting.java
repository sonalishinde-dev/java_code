package first;

public class Typecasting {

	public static void main(String[] args) {

		double i=234.234;
		
		int d=(int)i;
		
		System.out.println(d);
		
		int a=2325;
		
		double b=(double)a;
		
		System.out.println(b);
		
		int x=67;
		
		char y=(char)x;
		
		System.out.println(y);
		
		char n='s';
		
		int m=(char)n;
		
		System.out.println(m);
		
	}

}
